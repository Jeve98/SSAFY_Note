T = int(input())

for i in range(T):
    matrix = input().split()
    row = int(matrix[0])
    column = int(matrix[1])

    find = False
    ans = 0
    testing = 0

    for j in range(row):
        data = input()

        if find :
            continue

        # none code
        if(not '1' in data):
            continue
        # find code
        else:
            find = True
            reversedData = data[::-1]
            '''
            code    : num   : tempSum
            1011000 : 0     : 1 + 4 + 8 = 13
            1001100 : 1     : 1 + 8 + 16 = 25
            1100100 : 2     : 1 + 2 + 16 = 19
            1011110 : 3     : 1 + 4 + 8 + 16 + 32 = 61
            1100010 : 4     : 1 + 2 + 32 = 35
            1000110 : 5     : 1 + 16 + 32 = 49
            1111010 : 6     : 1 + 2 + 4 + 8 + 32 = 47
            1101110 : 7     : 1 + 2 + 8 + 16 + 32 = 59
            1110110 : 8     : 1 + 2 + 4 + 16 + 32 = 55
            1101000 : 9     : 1 + 2 + 8 = 11
            '''
            index = reversedData.find('1')

            count = 0
            odd = False
            addtional = 1 
            while count < 8:
                expSum = 0
                for k in range(7):
                    expSum += ((2 ** k) * int(reversedData[index + k]))
                
                if(odd):
                    addtional = 3
                    odd = not odd
                else:
                    addtional = 1
                    odd = not odd
                
                if expSum == 13 :
                    ans += 0
                    testing += 0*addtional
                elif expSum == 25:
                    ans += 1
                    testing += 1*addtional
                elif expSum == 19:
                    ans += 2
                    testing += 2*addtional
                elif expSum == 61:
                    ans += 3
                    testing += 3*addtional
                elif expSum == 35:
                    ans += 4
                    testing += 4*addtional
                elif expSum == 49:
                    ans += 5
                    testing += 5*addtional
                elif expSum == 47:
                    ans += 6
                    testing += 6*addtional
                elif expSum == 59:
                    ans += 7
                    testing += 7*addtional
                elif expSum == 55:
                    ans += 8
                    testing += 8*addtional
                elif expSum == 11:
                    ans += 9
                    testing += 9*addtional

                index += 7
                count += 1

    
    if(testing%10 == 0):
        print(f"#{i+1} {ans}")
    else:
        print(f"#{i+1} 0")

